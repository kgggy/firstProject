package basic;

public class VarExample {
	public static void main(String[] args) {
		int kor = 60;
		int eng;
		eng = 71;
		
		int sum = kor + eng;
		System.out.println("합: " + sum);
		
		sum = 100;
		
		double avg = sum / 2.0; // 65.5 
		System.out.println("평균: " + avg);
		
		System.out.println("두 수의 곱: " + kor * eng);
		
	}
}

